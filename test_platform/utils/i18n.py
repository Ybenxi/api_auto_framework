def _current_lang() -> str:
    """读取当前语言（query 参数优先），并同步到 session_state。"""
    query_lang = st.query_params.get("lang")
    if query_lang in ("zh", "en"):
        st.session_state["lang"] = query_lang
        return query_lang
    session_lang = st.session_state.get("lang")
    if session_lang in ("zh", "en"):
        return session_lang
    st.session_state["lang"] = "en"
    return "en"


def t(key: str, **kwargs) -> str:
    """返回当前语言的翻译文本，支持 format 参数"""
    lang = _current_lang()
    zh_dict = TRANSLATIONS["zh"]
    lang_dict = TRANSLATIONS.get(lang, zh_dict)
    text = lang_dict.get(key) or zh_dict.get(key, key)
    return text.format(**kwargs) if kwargs else text
